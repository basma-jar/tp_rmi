    private void bnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnUpdateActionPerformed
            int selectedRow = listMachines.getSelectedRow();
    if (selectedRow != -1) {
        try {
            String ref = txtRef.getText();
            String marque = txtMarque.getText();
            double prix = Double.parseDouble(txtPrix.getText());
            int id = (int) model.getValueAt(selectedRow, 0); // Get the ID from the selected row
             Salle salle = (Salle) jComboBox1.getSelectedItem();
            Machine updatedMachine = new Machine(id, ref, marque, prix,salle);
            dao.update(updatedMachine);
            load(); // Reload the table after updating
        } catch (RemoteException ex) {
            Logger.getLogger(MachineForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    }//GEN-LAST:event_bnUpdateActionPerformed

    private void bnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnDeleteActionPerformed
        try {
        int selectedRow = listMachines.getSelectedRow(); // Get the selected row
        if (selectedRow != -1) {
            int selectedId = (int) model.getValueAt(selectedRow, 0); // Get the ID from the selected row
            Machine m = dao.findById(selectedId); // Find the machine by ID
            dao.delete(m); // Delete the machine
            load(); // Reload the table after deleting
        }
    } catch (RemoteException ex) {
        Logger.getLogger(MachineForm.class.getName()).log(Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_bnDeleteActionPerformed

